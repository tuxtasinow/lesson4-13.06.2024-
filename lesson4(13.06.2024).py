my_string = input('Привет, я считаю количество символов в строке. Введите строку: ')
print(len(my_string))  #Длина строки

print(my_string.upper())  #Верхний регистр
print(my_string.lower())  #Нижний регистр
print(my_string.replace(' ', ''))  #Удаление пробелов
print(my_string[0])  #Первый символ
print(my_string[-1])  #Последний символ